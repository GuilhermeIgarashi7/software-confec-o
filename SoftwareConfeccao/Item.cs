namespace SoftwareConfeccao;

public class Item
{
    string nomeitem;

    string custoitem;

//--------------------------------------------------------------------------------------

    public void SetNomeItem(string nomeitem)
    {
        this.nomeitem = nomeitem;
    }

    public string GetNomeItem()
    {
        return nomeitem;
    }
//--------------------------------------------------------------------------------------

    public void SetCustoItem(string custoitem)
    {
        this.custoitem = custoitem;
    }

    public string GetNome()
    {
        return custoitem;
    }
}