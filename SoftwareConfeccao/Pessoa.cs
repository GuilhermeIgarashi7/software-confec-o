public class Pessoa
{
    string nome;
    int telefone;

    string email;


    public void SetNome(string nome)
    {
        this.nome = nome;
    }

    public string GetNome()
    {
        return nome;
    }

    public void SetTelefone(int telefone)
    {
        this.telefone = telefone;
    }    

    public int GetTelefone()
    {
        return telefone;
    }

    public void SetEmail(string email)
    {
        this.email = email;
    }    

    public string GetTelefone()
    {
        return email;
    }

}

